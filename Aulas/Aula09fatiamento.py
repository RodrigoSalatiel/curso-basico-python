frase = 'Curso em Vídeo Python'
print(frase)

#Funções de Fatiamento
print(frase[9])      #APENAS O CARACTER CORRESPONDENTE AO Nr 9(String inicia no 0)
print(frase[9:14])   #Inicia no 9 e va até o caracter 12(exclui o 13), entao devemos contar b+1 para somar os caracteres
print(frase[9:21:2]) #Começa no 9, para no 20 e vai pulando de 2 em 2
print(frase[:5])     #Vai do início até a letra
print(frase[15:])    #Indiquei o início e ele vai até o final, independente da quantidade de caractéres
print(frase[9::3])   #Vai iniciar no 9, vai até o final e pulando de 3 em 3





